def distribute_data_customized_iomt(attack_names, y_train, X_processed, seed=42):
    """
    Customized distribution for CIC-IoMT-2024 (2 agents).

    Agent 0: Benign + DoS/DDoS attacks
    Agent 1: Benign + MQTT + Recon + MITM attacks

    Args:
        attack_names: Array of attack name strings for each training sample
        y_train: Binary labels
        X_processed: Processed feature matrix
        seed: Random seed
    Returns:
        List of (X_agent, y_agent) tuples
    """
    categories = np.array([IOMT_ATTACK_CATEGORIES.get(name, 'Unknown')
                           for name in attack_names])

    # Agent 0: Benign + DoS/DDoS
    agent0_mask = np.isin(categories, ['Benign', 'DoS_DDoS'])
    # Agent 1: Benign + MQTT/Recon/MITM
    agent1_mask = np.isin(categories, ['Benign', 'MQTT_Recon_MITM'])

    agent_data = [
        (X_processed[agent0_mask], y_train[agent0_mask]),
        (X_processed[agent1_mask], y_train[agent1_mask]),
    ]

    for i, (X_a, y_a) in enumerate(agent_data):
        print(f"[Customized Distribution IoMT] Agent {i}: {len(X_a)} samples "
              f"(Benign: {np.sum(y_a==0)}, Attack: {np.sum(y_a==1)})")

    return agent_data



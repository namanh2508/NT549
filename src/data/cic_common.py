def preprocess_cic(train_df, test_df=None, test_size=0.2, seed=42):
    """
    Preprocess CIC dataset(s) for binary classification.

    If test_df is None, splits train_df into train/test (stratified).
    If test_df is provided (same or different domain), uses it directly.
    Scaler is always fitted on training data only.

    Handles CICFlowMeter data quality issues:
    - Replaces Infinity values with NaN (Flow Bytes/s, Flow Packets/s
      can be Inf when Flow Duration=0)
    - Fills NaN with training set medians
    - Clips scaled values to [0, 1]

    Args:
        train_df: Training DataFrame (with all 85 columns)
        test_df: Optional test DataFrame (for cross-dataset or explicit split)
        test_size: Train/test split ratio (used only when test_df is None)
        seed: Random seed
    Returns:
        X_train, y_train, X_test, y_test: numpy arrays (float32)
        scaler: fitted MinMaxScaler
        feature_dim: int (number of features, should be 79)
        train_attack_names: numpy array of attack name strings
        test_attack_names: numpy array of attack name strings
    """
    # Split if no test_df provided
    if test_df is None:
        train_df, test_df = train_test_split(
            train_df, test_size=test_size, random_state=seed,
            stratify=train_df[LABEL_COL]
        )
        train_df = train_df.reset_index(drop=True)
        test_df = test_df.reset_index(drop=True)

    # Extract labels (already binary 0/1)
    y_train = train_df[LABEL_COL].values.astype(int)
    y_test = test_df[LABEL_COL].values.astype(int)

    # Extract attack names (for customized distribution)
    train_attack_names = train_df[ATTACK_NAME_COL].values
    test_attack_names = test_df[ATTACK_NAME_COL].values

    # Drop metadata and label columns
    drop_cols = METADATA_COLS + [ATTACK_NAME_COL, LABEL_COL]
    train_features = train_df.drop(columns=drop_cols, errors='ignore')
    test_features = test_df.drop(columns=drop_cols, errors='ignore')

    # Align test columns to match train column order (defensive for cross-dataset)
    test_features = test_features.reindex(columns=train_features.columns)

    # Convert to float32
    train_features = train_features.astype(np.float32)
    test_features = test_features.astype(np.float32)

    # Handle Infinity and NaN values
    # CICFlowMeter produces Inf when Flow Duration=0 (division by zero)
    train_features = train_features.replace([np.inf, -np.inf], np.nan)
    test_features = test_features.replace([np.inf, -np.inf], np.nan)

    # Fill NaN with training set medians (train medians used for both)
    train_medians = train_features.median()
    train_features = train_features.fillna(train_medians)
    test_features = test_features.fillna(train_medians)

    n_train_nan = train_features.isna().sum().sum()
    n_test_nan = test_features.isna().sum().sum()
    if n_train_nan > 0 or n_test_nan > 0:
        print(f"  [Warning] Remaining NaN after fill: train={n_train_nan}, test={n_test_nan}")
        train_features = train_features.fillna(0)
        test_features = test_features.fillna(0)

    X_train = train_features.values
    X_test = test_features.values

    # Normalize with MinMaxScaler (fitted on training data only)
    scaler = MinMaxScaler()
    X_train = scaler.fit_transform(X_train)
    X_test = scaler.transform(X_test)

    # Clip test data to [0, 1] (cross-dataset values may exceed training range)
    X_test = np.clip(X_test, 0.0, 1.0)

    feature_dim = X_train.shape[1]

    print(f"[Preprocessing] Feature dimension: {feature_dim}")
    print(f"[Preprocessing] Training samples: {len(X_train)}")
    print(f"[Preprocessing] Testing samples: {len(X_test)}")
    print(f"[Preprocessing] Train labels: Benign={np.sum(y_train==0)}, "
          f"Attack={np.sum(y_train==1)}")
    print(f"[Preprocessing] Test labels: Benign={np.sum(y_test==0)}, "
          f"Attack={np.sum(y_test==1)}")

    return (X_train, y_train, X_test, y_test, scaler, feature_dim,
            train_attack_names, test_attack_names)



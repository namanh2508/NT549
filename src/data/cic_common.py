def resolve_cic_dataset_path(data_dir, dataset_name):
    """
    Resolve CIC dataset directory path.

    Searches local paths (data_dir) and common Kaggle input layouts.
    Kaggle normalizes dataset slugs to lowercase-hyphenated, so
    'CIC-BCCC-NRC-IoMT-2024' may appear as 'cic-bccc-nrc-iomt-2024'.

    Args:
        data_dir: Base directory containing dataset folders
        dataset_name: 'iomt' or 'iiot'
    Returns:
        Path to directory containing CSV files
    Raises:
        FileNotFoundError if dataset directory not found
    """
    dir_name = DATASET_DIRS[dataset_name]
    dir_name_lower = dir_name.lower()

    # Explicit candidate paths (most likely first)
    # Kaggle structure: /kaggle/input/<slug>/<OriginalFolderName>/*.csv
    candidates = [
        f'/kaggle/input/{dir_name_lower}/{dir_name}',   # standard Kaggle nested (slug/FolderName)
        f'/kaggle/input/{dir_name}/{dir_name}',         # mixed-case slug variant
        os.path.join(data_dir, dir_name),               # local: ./Dataset/CIC-BCCC-NRC-...
        f'/kaggle/input/{dir_name_lower}',              # flat Kaggle (CSVs directly in slug folder)
        os.path.join(data_dir, dataset_name),
        f'/kaggle/input/{dataset_name}',
    ]

    def _has_csvs(path):
        return os.path.isdir(path) and any(
            f.endswith('.csv') for f in os.listdir(path)
        )

    for path in candidates:
        if _has_csvs(path):
            return path

    # Fuzzy search: scan /kaggle/input and data_dir for matching subdirectories
    search_bases = ['/kaggle/input', data_dir]
    match_substrings = [dir_name_lower, dataset_name]

    for base in search_bases:
        if not os.path.isdir(base):
            continue
        for d in os.listdir(base):
            d_lower = d.lower()
            if not any(s in d_lower for s in match_substrings):
                continue
            full = os.path.join(base, d)
            # Check directory itself
            if _has_csvs(full):
                return full
            # Check one level deeper (e.g. /kaggle/input/slug/ActualDir/)
            if os.path.isdir(full):
                for sd in os.listdir(full):
                    subfull = os.path.join(full, sd)
                    if _has_csvs(subfull):
                        return subfull

    # List available datasets for debugging
    kaggle_input = '/kaggle/input'
    available = ''
    if os.path.isdir(kaggle_input):
        dirs = sorted(os.listdir(kaggle_input))
        available = '\n  Available in /kaggle/input: ' + ', '.join(dirs[:20])

    raise FileNotFoundError(
        f"Could not find {dir_name} dataset.\n"
        f"  Expected a directory with CSV files.\n"
        f"  Searched: {candidates}{available}"
    )


def load_cic_dataset(dataset_dir, subsample_per_file=None, seed=42):
    """
    Load all CSV files from a CIC dataset directory, concatenate into one DataFrame.

    Per-file subsampling caps the number of rows loaded from each CSV, which
    naturally reduces class imbalance (dominant classes get capped while small
    classes remain at full size).

    Args:
        dataset_dir: Path to directory containing CSV files
        subsample_per_file: Max rows per CSV file (None = load all)
        seed: Random seed for subsampling
    Returns:
        combined_df: Combined DataFrame with all data
    """
    csv_files = sorted(glob.glob(os.path.join(dataset_dir, '*.csv')))
    if not csv_files:
        raise FileNotFoundError(f"No CSV files found in {dataset_dir}")

    rng = np.random.default_rng(seed)
    frames = []

    for csv_path in csv_files:
        df = pd.read_csv(csv_path)
        n_original = len(df)

        if subsample_per_file is not None and n_original > subsample_per_file:
            idx = rng.choice(n_original, size=subsample_per_file, replace=False)
            df = df.iloc[idx].reset_index(drop=True)

        frames.append(df)
        fname = os.path.basename(csv_path)
        print(f"  [Load] {fname}: {n_original} rows"
              + (f" -> subsampled to {len(df)}" if len(df) < n_original else ""))

    combined = pd.concat(frames, ignore_index=True)
    print(f"  [Load] Total: {len(combined)} rows")

    # Print class distribution
    label_counts = combined[LABEL_COL].value_counts()
    print(f"  [Load] Benign: {label_counts.get(0, 0)}, "
          f"Attack: {label_counts.get(1, 0)}")

    return combined


